export type Language = 'vi' | 'en' | 'zh';
export type TranslationKey = keyof typeof translations.vi;

export const formatPrice = (price: number, language: Language): string => {
  if (language === 'vi') {
    return new Intl.NumberFormat('vi-VN', { style: 'currency', currency: 'VND' })
      .format(price)
      .replace('₫', '')
      .trim() + '₫';
  } else if (language === 'zh') {
    return new Intl.NumberFormat('zh-CN', { style: 'currency', currency: 'CNY' })
      .format(price / 3500); // Approximate conversion rate
  } else {
    return new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD' })
      .format(price / 24000); // Approximate conversion rate
  }
};

export const formatDiscount = (original: number, current: number): number => {
  if (!original || !current || original <= current) return 0;
  return Math.round((1 - current / original) * 100);
};
